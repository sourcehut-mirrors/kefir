#include "./definitions.h"

long char_to_long(char x) {
    return (long) x;
}

long short_to_long(short x) {
    return (long) x;
}

long int_to_long(int x) {
    return (long) x;
}

unsigned long uchar_to_ulong(unsigned char x) {
    return (unsigned long) x;
}

unsigned long ushort_to_ulong(unsigned short x) {
    return (unsigned long) x;
}

unsigned long uint_to_ulong(unsigned int x) {
    return (unsigned long) x;
}

_Bool long_to_bool(long x) {
    return (_Bool) x;
}

char long_to_char(long x) {
    return (char) x;
}

short long_to_short(long x) {
    return (short) x;
}

int long_to_int(long x) {
    return (int) x;
}

_Bool ulong_to_bool(unsigned long x) {
    return (_Bool) x;
}

unsigned char ulong_to_uchar(unsigned long x) {
    return (unsigned char) x;
}

unsigned short ulong_to_ushort(unsigned long x) {
    return (unsigned short) x;
}

unsigned int ulong_to_uint(unsigned long x) {
    return (unsigned int) x;
}
