__int128 i128_add(__int128 a, __int128 b) {
    return a + b;
}

unsigned __int128 u128_add(unsigned __int128 a, unsigned __int128 b) {
    return a + b;
}

__int128 i128_sub(__int128 a, __int128 b) {
    return a - b;
}

unsigned __int128 u128_sub(unsigned __int128 a, unsigned __int128 b) {
    return a - b;
}

__int128 i128_and(__int128 a, __int128 b) {
    return a & b;
}

unsigned __int128 u128_and(unsigned __int128 a, unsigned __int128 b) {
    return a & b;
}

__int128 i128_or(__int128 a, __int128 b) {
    return a | b;
}

unsigned __int128 u128_or(unsigned __int128 a, unsigned __int128 b) {
    return a | b;
}

__int128 i128_xor(__int128 a, __int128 b) {
    return a ^ b;
}

unsigned __int128 u128_xor(unsigned __int128 a, unsigned __int128 b) {
    return a ^ b;
}

__int128 i128_mul(__int128 a, __int128 b) {
    return a * b;
}

unsigned __int128 u128_mul(unsigned __int128 a, unsigned __int128 b) {
    return a * b;
}

__int128 i128_div(__int128 a, __int128 b) {
    return a / b;
}

unsigned __int128 u128_div(unsigned __int128 a, unsigned __int128 b) {
    return a / b;
}

__int128 i128_mod(__int128 a, __int128 b) {
    return a % b;
}

unsigned __int128 u128_mod(unsigned __int128 a, unsigned __int128 b) {
    return a % b;
}
